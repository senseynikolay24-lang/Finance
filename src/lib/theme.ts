// Единый источник цветовой системы приложения.
//
// PALETTE — категориальная палитра для графиков и цветовых меток: 8 различимых
// оттенков, проверенных на CVD-безопасность (иначе донаты сливаются в красное
// пятно). Порядок и значения не менять — они провалидированы.
export const PALETTE = [
  '#2a78d6', // 0 синий
  '#1baf7a', // 1 бирюзовый
  '#eda100', // 2 жёлтый
  '#008300', // 3 зелёный
  '#4a3aa7', // 4 фиолетовый
  '#E5383B', // 5 красный (фирменный акцент)
  '#e87ba4', // 6 розовый
  '#eb6834', // 7 оранжевый
] as const;

// Тематический цвет раздела — чтобы иконки, пустые состояния и графики одного
// раздела читались как одно целое.
export const SECTION_COLOR = {
  accounts: PALETTE[0], // синий
  deposits: PALETTE[1], // бирюзовый
  investments: PALETTE[4], // фиолетовый
  credits: PALETTE[7], // оранжевый
  goals: PALETTE[2], // жёлтый
  budget: PALETTE[3], // зелёный
} as const;

/** Добавляет alpha-канал к hex-цвету: withAlpha('#2a78d6', '1f') → '#2a78d61f'.
 *  '1f' ≈ 12%, '14' ≈ 8%, '0d' ≈ 5%, '33' ≈ 20%. */
export function withAlpha(hex: string, alpha = '1f'): string {
  return `${hex}${alpha}`;
}
